#!/usr/bin/env python3
import html
import json
import os
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
OUTPUT = ROOT / "output"
VIEWER = OUTPUT / "viewer.html"
INPUT = ROOT / "input" / "robot_figurine_cutout.png"


def rel(path: Path) -> str:
    return html.escape(os.path.relpath(path.resolve(), VIEWER.parent.resolve()))


def load_manifest(path: Path) -> dict:
    manifest = path / "manifest.json"
    if not manifest.exists():
        return {}
    return json.loads(manifest.read_text())


def model_card(title: str, glb: Path, manifest: dict) -> str:
    if not glb.exists():
        return f"""
        <section class="panel missing">
          <h2>{html.escape(title)}</h2>
          <p>Missing: <code>{html.escape(str(glb))}</code></p>
        </section>
        """
    elapsed = manifest.get("elapsed_sec")
    seed = manifest.get("seed")
    meta = []
    if seed is not None:
        meta.append(f"seed {seed}")
    if elapsed is not None:
        meta.append(f"{elapsed}s")
    meta_text = " · ".join(meta)
    return f"""
    <section class="panel">
      <h2>{html.escape(title)}</h2>
      <model-viewer src="{rel(glb)}" camera-controls auto-rotate rotation-per-second="18deg"
        environment-image="neutral" shadow-intensity="0.8" exposure="1.0"></model-viewer>
      <p class="meta">{html.escape(meta_text)}</p>
      <p><a href="{rel(glb)}">Open GLB</a></p>
    </section>
    """


def main() -> None:
    OUTPUT.mkdir(parents=True, exist_ok=True)
    trellis_dir = OUTPUT / "trellis2"
    hunyuan_dir = OUTPUT / "hunyuan3d21"
    trellis_manifest = load_manifest(trellis_dir)
    hunyuan_manifest = load_manifest(hunyuan_dir)

    mp4 = trellis_dir / "robot_trellis2.mp4"
    mp4_html = ""
    if mp4.exists():
        mp4_html = f"""
        <section class="panel">
          <h2>TRELLIS.2 Render</h2>
          <video src="{rel(mp4)}" controls loop muted autoplay playsinline></video>
          <p><a href="{rel(mp4)}">Open MP4</a></p>
        </section>
        """

